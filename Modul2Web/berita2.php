					<h2>Sarkozy dari Istana Élysée ke Penjara Paris: Eks-Presiden Memulai Hukuman</h2>

					<p><b>Paris, Prancis</b> – Mantan Presiden Prancis <b>Nicolas Sarkozy</b> (2007–2012) hari ini memasuki penjara di Paris 
					untuk memulai masa hukumannya setelah Mahkamah Agung menolak permohonan banding terakhirnya. Peristiwa ini menandai babak baru 
					yang mengejutkan dalam sejarah politik Prancis, karena Sarkozy menjadi mantan kepala negara kedua yang menjalani hukuman penjara 
					setelah persidangan yang berlarut-larut.</p>

					<p>Sarkozy, 70 tahun, dihukum karena tuduhan korupsi dan penyalahgunaan pengaruh terkait upaya ilegal untuk mendapatkan informasi 
					rahasia dari seorang hakim senior pada tahun 2014, yang dikenal sebagai <i>"kasus penyadapan"</i>. Ia dihukum tiga tahun penjara, 
					dengan dua tahun ditangguhkan, menjadikannya harus menjalani hukuman satu tahun.</p>

					<h3>Penolakan Banding Terakhir</h3>
					<p>Keputusan Mahkamah Agung untuk menolak bandingnya pada Senin malam telah menutup semua jalan hukum bagi mantan presiden tersebut. 
					Juru bicara pengadilan mengonfirmasi bahwa penolakan tersebut bersifat final dan segera berlaku.</p>

					<p>"Keputusan ini menegaskan kembali prinsip kesetaraan di hadapan hukum, sebuah pilar fundamental dari Republik ini, 
					bahwa bahkan mantan jabatan tertinggi negara pun tidak kebal dari penuntutan," kata seorang pakar hukum konstitusi, 
					Dr. Élise Moreau.</p>

