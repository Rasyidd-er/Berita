					memperkirakan pengumuman baru bisa dilakukan setelah akhir pekan.</p>

					<p>Dalam beberapa minggu terakhir, rumor berkembang bahwa sejumlah nama pelatih asing 
					tengah dipertimbangkan oleh Komite Teknis PSSI, termasuk pelatih dari Jepang dan Spanyol. 
					Sejumlah analis sepak bola nasional menilai keputusan ini krusial karena akan mempengaruhi 
					arah pembangunan timnas menuju Piala Asia dan kualifikasi Piala Dunia mendatang.</p>

					<p>Meski belum ada pengumuman resmi, sumber internal PSSI menyebut bahwa proses seleksi telah 
					memasuki tahap akhir. "Kami ingin memastikan pelatih yang terpilih memiliki visi jangka panjang, 
					bukan hanya hasil cepat," ujar salah satu anggota Exco PSSI.</p>

